public class AppData {
    private String[] name;
    private int[][] data;



    public void setName(String[] name) {
        this.name = name;
    }

    public void setData(int[][] data) {
        this.data = data;
    }

    public String[] getName() {
        return name;
    }

    public int[][] getData() {
        return data;
    }
}
