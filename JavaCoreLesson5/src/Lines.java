public class Lines {
    private int value;
    private  int value2;
    private  int value3;

    public Lines(int value, int value2, int value3){
        this.value = value;
        this.value2 = value2;
        this.value3 = value3;
    }

    public int getValue() {
        return value;
    }

    public int getValue2() {
        return value2;
    }

    public int getValue3() {
        return value3;
    }
}
